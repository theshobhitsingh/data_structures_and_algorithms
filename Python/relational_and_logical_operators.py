age=int(input("Enter your age:\n"))
# In and operator;both the conditions should be true
if(age>34 and age<50):
    print("That's Good!")
else:
    print("Not so good")

# In or operator;either of the conditions must be true
if(age>=34 or age<=50):
    print("That's Good!")
else:
    print("Not so good")