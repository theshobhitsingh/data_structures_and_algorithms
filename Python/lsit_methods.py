list=[12,122,1,0,11]
print(list)
list.sort() #Sorts the list
print(list)
list.reverse() #Reverses the list
print(list)
list.append(100000)  #Adds at the end of the list
print(list)
list.insert(3,10) #Inserts an item(10) at a specific index(3)
print(list)
list.pop(2)  #Removes an item from a particular index(2)
print(list)
list.remove(122) #Removes a particular item(122) from the list
print(list)