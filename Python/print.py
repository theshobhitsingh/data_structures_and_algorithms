# How to prevent print() to print in new lines
print("Namaste Ji!",end=" ")
print("Kaise hain aap log?",end=" ")
print("Main hu",end=" ")
print("Shobhit Singh",end="!!!")

#In Python, the end parameter is used in the print() function to specify what character or string should be printed at the end of the output. By default, the end parameter is set to '\n', which represents a newline character.