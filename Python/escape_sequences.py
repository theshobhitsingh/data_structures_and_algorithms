# Backslash
print("This is a backslash: \\")

# Single quote
print('This is a single quote: \'')

# Double quote
print("This is a double quote: \"")

# Newline
print("This is a newline:\nHello, World!")

# Horizontal tab
print("This is a horizontal tab:\tHello, World!")

# Carriage return
print("This is a carriage return:\rHello, World!")

# Backspace
print("This is a backspace:\bHello, World!")

# Form feed
print("This is a form feed:\fHello, World!")

# Octal value
print("This is an octal value:\110\145\154\154\157")

# Hexadecimal value
print("This is a hexadecimal value:\x48\x65\x6c\x6c\x6f")
