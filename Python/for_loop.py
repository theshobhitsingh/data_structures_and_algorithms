# range() function
for i in range(1,11):
    print(i)

# Printing the table of a number 
n = int(input("Enter a number: "))

for j in range(1, 11):
    result = n * j
    print(str(n) + " * " + str(j) + " = " + str(result))

# step-size --> is 1 by default
for k in range(1,11,2):
    print(k)
for i in range(1,100001):
    print(i)
