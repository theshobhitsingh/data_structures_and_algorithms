# Using procedural programming

a = 100
b = 200
print(f"The sum of {a} and {b} is {a+b}")

# USing object-oriented programming

class Number:
    def sum(self):
        return self.a + self.b

num = Number()
num.a = 12345
num.b = 67890
s = num.sum()
print(f"The sum of {num.a} and {num.b} is {s}")

